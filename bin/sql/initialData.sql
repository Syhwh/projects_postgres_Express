CREATE TABLE users(
user_id BIGINT UNIQUE NOT NULL GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
user_name  TEXT NOT NULL,
user_email TEXT NOT NULL,
user_password VARCHAR (255) NOT NULL
);

CREATE TABLE projects(
	project_id BIGINT UNIQUE NOT NULL GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
	name CHARACTER VARYING(20) NOT NULL,
	user_id BIGINT REFERENCES users(user_id)
);

CREATE TABLE tasks(
	task_id BIGINT UNIQUE NOT NULL GENERATED ALWAYS AS IDENTITY,
	name CHARACTER VARYING(20) NOT NULL,
	description CHARACTER VARYING(20) NOT NULL,
	status BOOLEAN NOT NULL DEFAULT FALSE,
	project_id BIGINT  REFERENCES projects(project_id)
);